#各変数に数値を代入
a=1
b=2
d=1.2
e=3.4
#変数を使って演算を行う
print("**　数値としての演算　**")
print("{}+{}={}".format(a,b,a+b))
print("{}+{}={}".format(d,e,d+e))
#str関数を使って数値を文字列に変換
a_s=str(a)
b_s=str(b)
d_s=str(d)
e_s=str(e)
#文字列としての演算を行う
print("**　文字列としての演算　**")
print("{}+{}={}".format(a_s,b_s,a_s+b_s))
print("{}+{}={}".format(d_s,e_s,d_s+e_s))

